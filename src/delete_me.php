                                        <p>&#8220;A Conversation with Rebecca Clarren,&#8221; Award-winning author
                                            Rebecca Clarren will discuss her acclaimed book, The Cost of Free Land:
                                            Jews, Lakota, and an American Inheritance, in a conversation moderated by
                                            UNO Assistant Professor, Susana Geliga, Ph.D., and Mikal Brotnov Eckstrom.
                                            The event is free and open to the public, thanks to the support of the
                                            Claire M. Hubbard Foundation and UNO's Division of Student Life and
                                            Wellbeing. This event will take place on April 4, 2024, at 6 P.M. <a
                                                href="https://events.unomaha.edu/samuel_bak_museum_the_learning_center_one_year_anniversary">RSVP
