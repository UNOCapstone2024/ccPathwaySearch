                                        <p><a
                                                href="https://www.unomaha.edu/samuel-bak-museum-the-learning-center/index.php">The
                                                Samuel Bak Museum: The Learning Center (SBMLC)</a> at the University of
                                            Nebraska at Omaha (UNO) proudly commemorates its first anniversary as a
                                            space for art, learning, and community engagement. </p>
                                        <p>Since its grand opening in February 2023, SBMLC has welcomed over 6,300
                                            visitors, including community members, K-12 students, and UNO faculty, staff
                                            and students. </p>
                                        <p>In 2019, UNO initiated a collaboration with renowned Holocaust survivor and
                                            artist, Samuel Bak, to showcase an exhibition of 70 original artworks on
                                            campus. The overwhelming response from the community spurred Bak's generous
                                            donation of a 512-piece collection to UNO. This act of philanthropy laid the
                                            foundation for what would become the SMBLC, dedicated to fostering
                                            understanding and empathy through art and education. </p>
                                        <p>Currently on display is the exhibition, "Becoming a Witness: From Abstraction
                                            to Figuration," which explores how Bak wrestled with bearing witness to the
                                            Holocaust and the role of memorials and monuments in today&#8217;s world. It
                                            explores how he embraced a broader mission to represent continued cycles of
                                            violence, genocide, and war and the importance of acknowledging the
                                            multiplicity of memories that form the fabric of American society.
                                            Complementing this exhibition are a range of free public programs, including
                                            monthly guest lectures, curator talks, and collaborations with local
                                            artists.</p>
                                        <br />
