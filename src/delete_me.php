                                <h5>About Samuel Bak Museum: The Learning Center</h5>
                                <p>The Samuel Bak Museum: The Learning Center, located at the University of Nebraska at
                                    Omaha, is dedicated to promoting understanding, empathy, and social justice through
                                    art and education. As a hub of cultural enrichment and community engagement, the
                                    museum offers a diverse range of exhibitions, programs, and resources for visitors
                                    of all ages and backgrounds. The Museum is currently located in a temporary location
                                    in Aksarben Village, plans are being made for a permanent space in the future on
                                    UNO&#8217;s campus. </p>





